function random(max) {
    // console.log("Hello");
    let randomNum = Math.floor(Math.random() * max);
    for (let i = 0; i < arr.length; i++) {
        if (randomNum === arr[i]) {
            randomNum = Math.floor(Math.random() * max);
            i = 0;
        }
    }
    // console.log(randomNum);
    arr[counter] = randomNum;
    // console.log(arr);
}